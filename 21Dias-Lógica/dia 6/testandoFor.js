//contagem até o número desejado

let num = parseInt(prompt("Digite um número inteiro e positivo:"))

for(let contagem = 0; contagem <= num; contagem++){
    console.log(contagem)
}

//contagem de 0 a 50 de 5 em 5

for(let contagem1 = 0; contagem1 <= 50; contagem1 += 5){
    console.log(contagem1)
}

//contagem de 0 a 50 de 5 em 5 decrescente

for(let contagem2 = 50; contagem2 >= 0; contagem2 -= 5){
    console.log(contagem2)
}